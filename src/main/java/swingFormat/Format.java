    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed

        try {
            connection = getConnection();
            ps = connection.prepareStatement("INSERT INTO personas (nombre, clave, domicilio, celular, correo, fecha_nacimiento, genero) VALUES (?,?,?,?,?,?,?)");
            ps.setString(1, txtNombre.getText());
            ps.setString(2, txtId.getText());
            ps.setString(3, txtDomicilio.getText());
            ps.setString(4, txtCelular.getText());
            ps.setString(5, txtCorreo.getText());
            ps.setDate(6, Date.valueOf(txtFechaNacimiento.getText()));
            ps.setString(7, (String) comboGenero.getSelectedItem());

            int result = ps.executeUpdate();

            if (result > 0) {
                JOptionPane.showMessageDialog(null, "Registro guardado con exito");
            } else {
                JOptionPane.showMessageDialog(null, "Error al guardar el registro");
            }

            connection.close();

        } catch (Exception ex) {
            System.out.println("Error, " + ex);
        }
        limpiar();
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        connection = getConnection();

        try {
            ps = connection.prepareStatement("SELECT * FROM personas WHERE clave = ?");
            ps.setString(1, txtBuscar.getText());
            rs = ps.executeQuery();
            if (rs.next()) {
                txtHiddenId.setText(rs.getString("id_persona"));
                txtId.setText(rs.getString("clave"));
                txtNombre.setText(rs.getString("nombre"));
                txtDomicilio.setText(rs.getString("domicilio"));
                txtCelular.setText(rs.getString("celular"));
                txtCorreo.setText(rs.getString("correo"));
                txtFechaNacimiento.setText(rs.getString("fecha_nacimiento"));
                comboGenero.setSelectedItem(rs.getString("genero"));
            }
        } catch (Exception e) {
            System.out.println("Error, " + e);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        connection = getConnection();
        int updateResult = 0;
        try {
            
            ps = connection.prepareStatement("UPDATE personas SET nombre=?, domicilio=?, celular=?, correo=?, fecha_nacimiento=?, genero=? WHERE id_persona=?;");
            ps.setString(1, txtNombre.getText());
            ps.setString(2, txtDomicilio.getText());
            ps.setString(3, txtCelular.getText());
            ps.setString(4, txtCorreo.getText());
            ps.setDate(5, Date.valueOf(txtFechaNacimiento.getText()));
            ps.setString(6, (String) comboGenero.getSelectedItem());
            ps.setString(7, txtHiddenId.getText());
            
            updateResult = ps.executeUpdate();
        } catch (Exception e) {
            System.out.println("Error, " + e);
        }

        if (updateResult > 0) {
            JOptionPane.showMessageDialog(null, "Registro actualizado");
        } else {
            JOptionPane.showMessageDialog(null, "Error al actualizar registro");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

